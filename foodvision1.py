loss_fn = nn.CrossEntropyLoss()
results = {}

def train_and_test(model, name, epochs=5, lr=0.001):
    model.to(device)
    optimizer = torch.optim.Adam(
        filter(lambda p: p.requires_grad, model.parameters()), lr=lr
    )

    # Training
    for epoch in range(epochs):
        model.train()
        running_loss = 0
        for imgs, labels in train_dataloader:
            imgs, labels = imgs.to(device), labels.to(device)
            optimizer.zero_grad()
            loss = loss_fn(model(imgs), labels)
            loss.backward()
            optimizer.step()
            running_loss += loss.item()
        print(f"{name} | Epoch {epoch+1}/{epochs} | Loss: {running_loss/len(train_dataloader):.4f}")

    # Testing
    model.eval()
    correct, total, test_loss = 0, 0, 0
    with torch.no_grad():
        for imgs, labels in test_dataloader:
            imgs, labels = imgs.to(device), labels.to(device)
            preds = model(imgs)
            test_loss += loss_fn(preds, labels).item()
            correct += (preds.argmax(1) == labels).sum().item()
            total += len(labels)

    acc = correct / total
    avg_loss = test_loss / len(test_dataloader)
    results[name] = {"accuracy": acc, "loss": avg_loss}
    print(f" {name} | Accuracy: {acc:.2%} | Loss: {avg_loss:.4f}\n")

    # Save and clean up
    save_path = f"/content/drive/MyDrive/FoodVision/{name}.pth"
    torch.save(model.state_dict(), save_path)
    print(f" Saved to {save_path}")
    del model
    torch.cuda.empty_cache()

