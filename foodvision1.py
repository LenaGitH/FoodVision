"""### Getting predictions

#### Creating a function for prediction allocation

This function is used to:

1. Run the model on the data
2. Collect the predictions
3. Collect the true labels
4. Collect the probability scores for each class
5. Return everything ready for analysis or evaluation
"""

import numpy as np
def get_predictions(model, dataloader):

  """
                  This function is going to run the model on the data and
                  collect the predictions with the true labels and
                   the probability scores for each class then
                  return everything """

  all_preds, all_labels, all_probs = [], [], []
  model.eval()
  with torch.no_grad():
        for images, labels in dataloader:
            images = images.to(device)

            # Forward pass
            outputs = model(images)

            # Convert outputs to probabilities
            probs = torch.softmax(outputs, dim=1)

            ## Get predicted labels
            all_preds.extend(outputs.argmax(1).cpu().numpy())

            # Save true labels
            all_labels.extend(labels.numpy())

            # Save probabilities
            all_probs.extend(probs.cpu().numpy())

  return np.array(all_preds), np.array(all_labels), np.array(all_probs)

