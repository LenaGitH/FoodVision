def measure_speed(model, loader, n=10):
    model.eval()
    start = time.time()
    with torch.no_grad():
        for i, (imgs, _) in enumerate(loader):
            if i >= n: break
            _ = model(imgs.to(device))
    return (time.time() - start) / n

