# Loading the model
model = torchvision.models.resnet50(weights=None)
model.fc = nn.Linear(2048, 101)
model.load_state_dict(model.load_state_dict(torch.load('ResNet50-FineTuned.pth', map_location=device)))
model.eval()

test_transform = transforms.Compose([
    transforms.Resize((224, 224)),
    transforms.ToTensor(),
    transforms.Normalize([0.485, 0.456, 0.406], [0.229, 0.224, 0.225])
])

def predict(image):
    img_tensor = test_transform(image).unsqueeze(0)
    with torch.no_grad():
        probs = torch.softmax(model(img_tensor), dim=1)[0]
    return {class_names[i]: float(probs[i]) for i in range(len(class_names))}

demo = gr.Interface(
    fn=predict,
    inputs=gr.Image(type="pil"),
    outputs=gr.Label(num_top_classes=5),
    title="FoodVision — Food Classifier",
    description="Upload a food image to classify it into 101 categories."
)
demo.launch()